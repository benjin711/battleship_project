#include "ship.h"

/*
 * Constructor Class Ship
 */

Ship::Ship(std::string Nname, qint8 Nhp, qint8 Nlength)
    : _name(Nname), _hp(Nhp), _sunk(0), _placed(0), _length(Nlength)
{
    for(qint8 i=0; i < Nlength; i++)
    {
        _coordinates.push_back(std::pair<qint8, qint8> {0,0});
    }
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

qint8& Ship::getHp()
{
    return _hp;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

std::string& Ship::getName()
{
    return _name;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool& Ship::getSunk()
{
    return _sunk;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool& Ship::getPlaced()
{
    return _placed;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

qint8 Ship::getLength()
{
    return _length;
}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

std::vector<std::pair<qint8,qint8> >& Ship::getCoordinates()
{
    return _coordinates;
}









